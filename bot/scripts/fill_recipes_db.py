def fill_db():
    """Наполняет базу данных рецептами."""
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()

    # Очищаем таблицу
    cursor.execute("DELETE FROM recipes")

    # Вставляем рецепты
    for recipe in RECIPES:
        cursor.execute("""
            INSERT INTO recipes (title, ingredients, instructions, category, source)
            VALUES (?, ?, ?, ?, ?)
        """, (recipe['title'], recipe['ingredients'], recipe['instructions'], recipe.get('category', 'выпечка'), 'база'))

    conn.commit()
    count = cursor.execute("SELECT COUNT(*) FROM recipes").fetchone()[0]
    conn.close()

    print(f"✅ Добавлено {count} рецептов в базу данных: {DB_PATH}")


if __name__ == "__main__":
    fill_db()
