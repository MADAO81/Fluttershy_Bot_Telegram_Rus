def fill_db():
    """Наполняет базу данных животными."""
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()

    # Очищаем таблицу
    cursor.execute("DELETE FROM animals")

    # Вставляем животных
    for animal in ANIMALS:
        cursor.execute("""
            INSERT INTO animals (name, scientific_name, habitat, diet, fun_fact, description, conservation_status)
            VALUES (?, ?, ?, ?, ?, ?, ?)
        """, (animal['name'], animal['scientific_name'], animal['habitat'], animal['diet'], animal['fun_fact'], animal['description'], animal['conservation_status']))

    conn.commit()
    count = cursor.execute("SELECT COUNT(*) FROM animals").fetchone()[0]
    conn.close()

    print(f"✅ Добавлено {count} животных в базу данных: {DB_PATH}")


if __name__ == "__main__":
    fill_db()
